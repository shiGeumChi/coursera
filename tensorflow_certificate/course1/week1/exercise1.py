    model = keras.Sequential([keras.layers.Dense(units=1,input_shape=[1])])# Your Code Here#
    model.compile(optimizer='sgd', loss = 'mean_squared_error')# Your Code Here#
    model.fit(xs,ys,epochs=500)# Your Code here#
